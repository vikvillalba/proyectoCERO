    private void tblClasesExistentesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClasesExistentesMouseClicked
        // TODO add your handling code here:
        int column = this.tblClasesExistentes.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / this.tblClasesExistentes.getRowHeight();

        if (row < this.tblClasesExistentes.getRowCount() && row >= 0 && column < this.tblClasesExistentes.getColumnCount() && column >= 0) {
            Object value = this.tblClasesExistentes.getValueAt(row, column);
            if (value instanceof JButton) {
                JButton boton = (JButton) value;
                int idClase = (int) this.tblClasesExistentes.getValueAt(row, 0);

                if (boton.getText().equals("Seleccionar")) {
                    seleccionarClase(idClase);
                }
            }
        }

    }//GEN-LAST:event_tblClasesExistentesMouseClicked

